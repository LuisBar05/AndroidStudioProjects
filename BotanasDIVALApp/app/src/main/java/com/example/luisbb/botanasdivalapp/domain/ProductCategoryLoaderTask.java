package com.example.luisbb.botanasdivalapp.domain;

import android.content.Context;
import android.os.AsyncTask;
import android.support.v7.widget.RecyclerView;
import android.view.View;
import android.widget.TextView;

import com.example.luisbb.botanasdivalapp.R;
import com.example.luisbb.botanasdivalapp.utils.AppConstants;
import com.example.luisbb.botanasdivalapp.utils.Utils;
import com.example.luisbb.botanasdivalapp.utils.Utils.AnimationType;
import com.example.luisbb.botanasdivalapp.views.activities.ECartHomeActivity;
import com.example.luisbb.botanasdivalapp.views.adapter.CategoryListAdapter;
import com.example.luisbb.botanasdivalapp.views.adapter.CategoryListAdapter.OnItemClickListener;
import com.example.luisbb.botanasdivalapp.views.fragment.ProductOverviewFragment;

/**
 * The Class ImageLoaderTask.
 */
public class ProductCategoryLoaderTask extends AsyncTask<String, Void, Void> {

    private static final int NUMBER_OF_COLUMNS = 2;
    private Context context;
    private RecyclerView recyclerView;
    private TextView banner;

    public ProductCategoryLoaderTask(RecyclerView listView, Context context, TextView banner) {

        this.recyclerView = listView;
        this.context = context;
        this.banner=banner;
    }

    @Override
    protected void onPreExecute() {

        super.onPreExecute();

        if (null != ((ECartHomeActivity) context).getProgressBar())
            ((ECartHomeActivity) context).getProgressBar().setVisibility(
                    View.VISIBLE);

    }

    @Override
    protected void onPostExecute(Void result) {
        super.onPostExecute(result);

        if (null != ((ECartHomeActivity) context).getProgressBar())
            ((ECartHomeActivity) context).getProgressBar().setVisibility(
                    View.GONE);

        if (recyclerView != null) {
            CategoryListAdapter simpleRecyclerAdapter = new CategoryListAdapter(
                    context);

            recyclerView.setAdapter(simpleRecyclerAdapter);

            simpleRecyclerAdapter
                    .SetOnItemClickListener(new OnItemClickListener() {

                        @Override
                        public void onItemClick(View view, int position) {

                            AppConstants.CURRENT_CATEGORY = position;

                            Utils.switchFragmentWithAnimation(
                                    R.id.frag_container,
                                    new ProductOverviewFragment(),
                                    ((ECartHomeActivity) context), null,
                                    AnimationType.SLIDE_LEFT);

                        }
                    });
        }

    }

    @Override
    protected Void doInBackground(String... params) {

        try {
            Thread.sleep(3000);
        } catch (InterruptedException e) {
            e.printStackTrace();
        }

        return null;
    }

}